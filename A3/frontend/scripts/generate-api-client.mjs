  "",
  'export function createApiClient({ baseUrl = DEFAULT_BACKEND_URL, getToken } = {}) {',
  '  async function request({ operationId, path, method, pathParams, query, body, headers: customHeaders = {}, token, signal, ...rest } = {}) {',
  "    const headers = new Headers(customHeaders);",
  '    const routeMeta = operationId ? routeManifest[operationId] : undefined;',
  '    const resolvedToken = token ?? getToken?.();',
  "",
  '    if (resolvedToken && !headers.has("Authorization")) {',
  '      headers.set("Authorization", `Bearer ${resolvedToken}`);',
  "    }",
  "",
  '    const response = await fetch(buildUrl(baseUrl, path, pathParams, query), {',
  "      method,",
  "      headers,",
  '      body: buildRequestBody(body, headers, routeMeta),',
